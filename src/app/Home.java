    @Override
    public void tampilkanPesanan() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama");
        model.addColumn("Maskapai");
        model.addColumn("Kode Maskapai");
        model.addColumn("Keberangkatan");
        model.addColumn("Tujuan");
        model.addColumn("Tanggal");
        model.addColumn("Jam");
        
        try {

            String sql = "SELECT * FROM pesanan WHERE email='" + akun.getEmail() + "'";
            java.sql.Connection conn = (Connection) Koneksi.konfigDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[] {res.getString(2), res.getString(3), res.getString(4),res.getString(5), res.getString(6), res.getString(7), res.getString(8)});
            }
            tabelPesanan.setModel(model);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

